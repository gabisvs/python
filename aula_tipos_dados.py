"""
Tipos de dados básicos:

int - Números inteiros
float - Números com casas decimais
string - TUDO que for texto
boolean - Tipo lógico com apenas 2 valores: True e False
"""

# int
print(10)
print(type(10))
print(30500, type(30500))
print(-30500, type(-30500))

# float
print()
print(10.0, type(10.0))
print(3.14, type(3.14))
print(30500.900, type(30500.900))
print(-30500.900, type(-30500.900))

# Números Complexos
print(2 + 2j, type(2 + 2j))

# string
print()
print("olá mundo", type("olá mundo"))
print('olá mundo', type('olá mundo'))
print("11.111.111-11", type("11.111.111-11"))
print("senha123", type("senha123"))
print("print('olá mundo')", type("print('olá mundo')"))
print("print(\"olá mundo\")", type("print('olá mundo')"))

print("oi \n tudo bem?")
print("oi \t tudo bem?")
print("oi \r tudo bem?")

# boolean (bool)
print()
print(True, type(True))
print(False, type(False))
